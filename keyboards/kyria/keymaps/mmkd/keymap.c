#ifdef ENCODER_ENABLE
void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if(IS_LAYER_ON(_SWAP)) {
            if (clockwise) {
                tap_code(KC_GRV);
            } else {
                tap_code16(LSFT(KC_GRV));
            }
        } else {
            // Volume control
            if (clockwise) {
                tap_code(KC_VOLU);
            } else {
                tap_code(KC_VOLD);
            }
        }
    }
    else if (index == 1) {
        if(IS_LAYER_ON(_SWAP)) {
            if (clockwise) {
                tap_code(KC_GRV);
            } else {
                tap_code16(LSFT(KC_GRV));
            }
        } else {
            // Page up/Page down
            if (clockwise) {
                tap_code(KC_TAB);
            } else {
                tap_code16(LSFT(KC_TAB));
            }
        }
    }
}
#endif
